// Generate public/_redirects mapping /y/{shortId} → canonical article URL.
// Cloudflare Pages reads _redirects from build output root to apply 301s.
// Runs as `prebuild` so the file is present when astro build copies public/.

import { readdirSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname } from 'node:path';
import { shortIdForSlug } from '../src/lib/short-url.mjs';

const ARTICLES_DIR = 'src/content/articles';
const OUT_FILE = 'public/_redirects';

const slugs = readdirSync(ARTICLES_DIR)
  .filter((f) => f.endsWith('.mdx'))
  .map((f) => f.replace(/\.mdx$/, ''));

const lines = [
  '# Auto-generated by scripts/gen-redirects.mjs — do not edit manually.',
  '# Short share URLs: /y/{YYMMDD}{a|b|...} → canonical article slug (301).',
  '# Used by LINE/FB share buttons so the visible URL is short, not the',
  '# 100+ byte URL-encoded Chinese slug.',
  '',
];

for (const slug of slugs.sort()) {
  const id = shortIdForSlug(slug, slugs);
  lines.push(`/y/${id}  /${slug}/  301`);
}

mkdirSync(dirname(OUT_FILE), { recursive: true });
writeFileSync(OUT_FILE, lines.join('\n') + '\n');
console.log(`✓ wrote ${slugs.length} short-URL redirects to ${OUT_FILE}`);
